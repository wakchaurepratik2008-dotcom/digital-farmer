export const getPestByCrop = (cropName) => {
  return pestData[cropName] || [];
};

export const getAllPests = () => {
  const allPests = [];
  Object.keys(pestData).forEach(crop => {
    pestData[crop].forEach(pest => {
      allPests.push({ ...pest, crop });
    });
  });
  return allPests;
};

export const searchPests = (searchTerm, filters = {}) => {
  const allPests = getAllPests();
  
  return allPests.filter(pest => {
    const matchesSearch = !searchTerm || 
      pest.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
      pest.scientificName.toLowerCase().includes(searchTerm.toLowerCase()) ||
      pest.description.toLowerCase().includes(searchTerm.toLowerCase());
    
    const matchesType = !filters.type || filters.type === 'all' || pest.type === filters.type;
    const matchesSeverity = !filters.severity || filters.severity === 'all' || pest.severity === filters.severity;
    const matchesSeason = !filters.season || filters.season === 'all' || pest.season === filters.season;
    const matchesCrop = !filters.crop || filters.crop === 'all' || pest.crop === filters.crop;
    
    return matchesSearch && matchesType && matchesSeverity && matchesSeason && matchesCrop;
  });
};

export const getSeverityColor = (severity) => {
  switch(severity) {
    case 'उच्च': return '#f44336';
    case 'मध्यम': return '#ff9800';
    case 'कमी': return '#4caf50';
    default: return '#666';
  }
};

export const getTypeIcon = (type) => {
  switch(type) {
    case 'किडा': return 'bug';
    case 'रोग': return 'exclamation-triangle';
    default: return 'info-circle';
  }
};
